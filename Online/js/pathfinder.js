// Function changes app state to be "selecting/setting" a start or stop position
// this state highlights grid cells when moused over with the color of the 
// position state i.e. start green and stop red. 
function SettingStartStop(value){
	
	// do not display the pathfind button container
    window.pathfind_button_container.style.display = 'none';

    // if an existing path was previously found
    // clear the grid because this is a new path
    if(window.app_state === 2){
        ClearGrid();
    }

    // make sure the value is round... why? 
    // Just in case I get weird and pass a float
    value = Math.round(value);

    // if the value is 0 || 1
    if(value > -1 && value < 2){
        window.app_state = value; 
    }else{ // incorrect value
        window.app_state = 0; // default to setting start
    }

    // if we're setting the start position
    if(window.app_state === 0){    
        // for all the grid buttons
        window.grid_buttons.forEach(function(button){
           // add/remove the appropriate css classes
           document.getElementById(button).classList.remove("grid_button_start");
           document.getElementById(button).classList.remove("selecting_stop");
           document.getElementById(button).classList.add("selecting_start");
        });    
    }
    // if we're setting the stop position
    else if(window.app_state === 1){
        // for all the grid buttons
        window.grid_buttons.forEach(function(button){
            // add/remove the appropriate css classes
            document.getElementById(button).classList.remove("grid_button_stop");
            document.getElementById(button).classList.remove("selecting_start");
            document.getElementById(button).classList.add("selecting_stop");
        });
    }
    // hypothetical room for expansion or a generic/default else action here
}


// A grid button has been clicked/selected
function Select(selected_button){

    var start_set = false;
    var stop_set = false;

    // if we're selecting the start position
    if(window.app_state === 0){
        // for all the grid buttons
        window.grid_buttons.forEach(function(button){
           // remove the appropriate css classes
           document.getElementById(button).classList.remove("grid_button_start");
           document.getElementById(button).classList.remove("selecting_start");
        });
        // add the appropriate css class
        document.getElementById(selected_button.id).classList.add("grid_button_start");
    }
    // if we're selecting the stop position
    else if(window.app_state === 1){
        // for all the grid buttons
        window.grid_buttons.forEach(function(button){
            // remove the appropriate css classes
            document.getElementById(button).classList.remove("grid_button_stop");
            document.getElementById(button).classList.remove("selecting_stop");
        });
        // add the appropriate css class
        document.getElementById(selected_button.id).classList.add("grid_button_stop");
    }


    // Have we set a start and a stop position?
    Object.keys(window.grid_buttons).forEach(function (key) {
        var button = window.grid_buttons[parseInt(key, 10)];
        var is_start = document.getElementById(button).classList.contains("grid_button_start");
        var is_stop = document.getElementById(button).classList.contains("grid_button_stop");

        if(is_start){
            window.start_pos = parseInt(key, 10);
        }
        else if(is_stop){
            window.stop_pos = parseInt(key, 10);
        }

        if(window.start_pos !== null && window.stop_pos !== null){
            window.pathfind_button_container.style.display = 'block';
        }

    });

    // we are no longer selecting a start or stop position
    window.app_state = -1;
}


function ClearGrid(){
    // for all the grid buttons
    window.grid_buttons.forEach(function(button){
        // remove the appropriate css classes
        document.getElementById(button).classList.remove("grid_button_start");
        document.getElementById(button).classList.remove("grid_button_stop");
        document.getElementById(button).classList.remove("grid_button_step");
        document.getElementById(button).classList.remove("selecting_start");
        document.getElementById(button).classList.remove("selecting_stop");

        // clear position values
        window.start_pos = null;
        window.stop_pos = null;

        // we are no longer selecting a start or stop position
        window.app_state = -1;
    });
	
	window.pathfind_button_container.style.display = 'none';
}


function TrainPathfinder(){
    NN = FANN.create([num_input, num_neurons_hidden, num_output]);

    NN.set_activation_steepness_hidden(0.01);
    NN.set_activation_steepness_output(0.01);
    NN.set_activation_function_hidden(FANN.SIGMOID_SYMMETRIC);
    NN.set_activation_function_output(FANN.SIGMOID_SYMMETRIC);
    //NN.set_train_stop_function(FANN.STOPFUNC_BIT);
    //NN.set_bit_fail_limit(0.01);

    NN.set_train_stop_function(FANN.STOPFUNC_MSE);

    NN.set_training_algorithm(FANN.TRAIN_RPROP);

    data = FANN.createTraining(PATHFINDER_TRAINING_DATA);
    //NN.init_weights(data);
    NN.randomize_weights(-1, 1);

    // Train
    NN.train_on_data(data, max_epochs, epochs_between_reports, desired_error);

    return NN;
}


function Pathfind(){
    // New array full of zeros
    var input = new Array(25).fill(0);

    // Add start and stop positions to the grid
    input[window.start_pos] = 1;
    input[window.stop_pos] = 1;

    // Pathfind
    var result = window.ANN.run(input);

    // update grid
    for (var i = 0; i <= 24; i++){
		
        // If result[i] is greater than zero and it rounds up to one (0.5 or greater)
        if(result[i] > 0 && Math.round(result[i]) == 1) {
           
		   
           if(i != window.start_pos || i != window.stop_pos){
               document.getElementById(window.grid_buttons[i]).classList.add("grid_button_step");
           }
		   
        }
    }
    
    // do not display the pathfind button container
    window.pathfind_button_container.style.display = 'none';
    
    window.app_state = 2;
}




